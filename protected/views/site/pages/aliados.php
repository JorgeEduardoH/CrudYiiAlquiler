<div class="aliados-center"><h2>Parques Nacionales Naturales de Colombia</h2></div>

<br><br>
<p>
Colombia es reconocido en el mundo por su rica biodiversidad: ocupa el primer

lugar en aves y anfibios, el segundo en plantas, el tercero en reptiles y el quinto en

mamíferos. También es inmensamente rico en recursos hídricos; su mar territorial

es tan extenso como su área continental, y es el cuarto país con mayores

precipitaciones anuales.</p>



<p>Esta riqueza natural está representada en 59 Parques Nacionales Naturales,

áreas protegidas que prestan servicios invaluables a los colombianos. Suministran

el agua necesaria para el riego de cultivos, los procesos industriales y la vida

diaria. Ayudan a regular los regímenes de lluvias y a fijar el carbono, manteniendo

la estabilidad del clima. Previenen y mitigan los efectos de desastres naturales

como tsunamis, inundaciones, deslizamientos. También conservan la diversidad

genética que permite encontrar nuevas medicinas. Son centros de dispersión de

plantas alimenticias y garantizan recursos pesqueros a poblaciones costeras. Son

espacios hermosos que regocijan el espíritu y nos permiten despejar la mente y

ejercitar el cuerpo, manteniéndonos sanos y felices.</p>
<div class="aliados-center">
<img  src="<?php echo Yii::app()->baseUrl;?>/images/Logo_parquesnaturales.png"/></div>

<p>Los parques son administrados por Parques Nacionales Naturales de Colombia,

una entidad que hace parte del Ministerio de Ambiente y Desarrollo Territorial, que

tiene el compromiso de conservar in situ la diversidad biológica del país,

representada en las 59 áreas de carácter nacional que abarcan una extensión de

14.268.224 hectáreas y coordina el Sistema Nacional de Áreas Protegidas,

Sinap— con 23.810.280 hectáreas, que comprenden las áreas protegidas tanto

públicas como privadas, del orden regional y nacional.</p>

<p>En los parques se conserva el 75% de las lagunas y ciénagas y el 62% de los

acuíferos. La conservación del recurso hídrico del país es quizás uno de los

beneficios más evidentes de las áreas del Sistema de Parques: muchos

ecosistemas que se protegen en ellas suministran el agua necesaria para el riego

de cultivos, los procesos industriales y la vida diaria. Unos veinticinco millones de

personas dependen directa o indirectamente del agua que suministran nuestras

áreas protegidas.</p>

<p>En el Sistema de Parques existen áreas abiertas al ecoturismo, en las cuales el

visitante recibe charlas informativas y educativas donde aprende la importancia de

la conservación y el papel que cumplen cada una de las especies animales y

vegetales en su hábitat. Estas actividades de turismo de naturaleza son una

experiencia única para la vida de los colombianos y para muchos extranjeros que

acuden a ellos para la observación de aves, el avistamiento de ballenas, el buceo,

las caminatas y el camping y son también importantes fuentes de ingreso para las

comunidades que se benefician del impulso que recibe la economía local con la

llegada de los visitantes.</p>

<p>A través del Sistema de Parques Nacionales Naturales el Estado hace un valioso

aporte al cuidado del patrimonio natural y cultural de todos los colombianos. Pero

es necesario también que la ciudadanía acompañe esta titánica labor que significa

la conservación de estas áreas naturales productoras de agua y de otros servicios

indispensables para la vida.</p>

    <div class="aliados-center"> <h2>AFACOM </h2>
<br><br>
  <img  src="<?php echo Yii::app()->baseUrl;?>/images/AFACOM.jpg"/></div>
<p>Es una asociación académica sin ánimo de lucro fundada en Bogotá en 1979, que cuenta con reconocimiento nacional e internacional, y está constituida por las Facultades o Departamentos oficialmente autorizados, para ofrecer enseñanza integral en el campo universitario, en las áreas de Comunicación e Información. </p>
 
<p>Dichas instituciones están representadas por los Decanos de las facultades o los Directores de los departamentos o programas. Actualmente, la Asociación agrupa a 43 Facultades de Comunicación de Colombia.</p> 
 
<p>AFACOM forma parte de la Federación Latinoamericana de Facultades de Comunicación Social -FELAFACS- y en esta medida sus proyectos y resultados cuentan con el apoyo y cooperación de la comunidad iberoamericana.</p>    
 
<p>Igualmente, AFACOM  busca promover el desarrollo de programas de investigación y de formación docente, así como orientar a las facultades y departamentos, para que sus niveles de formación estén en correspondencia con las tendencias y necesidades académicas y sociales y las proyecciones de la profesión en las esferas nacional e internacional.
 </p>
<p>Agradecemos al Dr. Rafael Gonzáles Pardo, Presidente de AFACOM por brindarnos su apoyo tener cada día mayor participación en la categoría estudiantes.</p>  



                

